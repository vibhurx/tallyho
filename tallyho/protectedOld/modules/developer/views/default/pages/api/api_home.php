<h2>What are Tallyho APIs</h2>
<p>
Organizers who have their own website and web-applications can integrate with Tallyho event engine using APIs provided by us. It would require
the organizers to register with us and use their username and password to access and modify the data using API calls. Other 
software or application vendors can also access APIs on behalf of their users (organizers or players) and access the APIs.
</p>

Currently the APIs are under development and alpha-stage. Most of the APIs are read-only which should not stop you from using them immediately.
